//! Option A consumption: generate a per-model `list.html` override that the
//! framework serves via `RUSTIO_TEMPLATE_DIR` (the same seam navigation uses).
//!
//! Doctrine note (see DESIGN_REASONING R-001 / R-002): rustio-admin forbids a
//! schema-driven *runtime* — presentation is reshaped at build time, never by a
//! framework hook. So we emit a template, not data, exactly like `_sidebar.html`.
//!
//! The framework's `list.html` chrome (search, filters, sort, bulk, pagination,
//! empty state) is reproduced **verbatim** from the template we target — those
//! sections depend only on framework context (`filters`, `sort_fields`, `rows`,
//! …), never on the column set. Only the two `{% for f in fields %}` column
//! loops (header + body cell) are replaced with view-spec-driven columns. This
//! keeps every non-column feature byte-identical to the framework and isolates
//! drift to a single, testable seam.
//!
//! Targets rustio-admin's `assets/templates/admin/list.html` contract. The
//! version it mirrors is recorded in `LIST_TEMPLATE_BASED_ON`; bump it when the
//! framework's list chrome changes.

use crate::spec::{TableView, ViewCell};

/// The framework `list.html` version this override mirrors (for drift tracking).
pub const LIST_TEMPLATE_BASED_ON: &str = "rustio-admin list.html · console masthead + data board";

/// Build the per-model `list.html` override for one table view.
pub fn build_list_template(project: &str, view: &TableView) -> String {
    // Visible cells lead the list (primary first, then secondary in declared
    // order). Detail/hidden never appear in the list mode.
    let mut visible: Vec<&ViewCell> = view
        .cells
        .iter()
        .filter(|c| c.role == "primary" || c.role == "secondary")
        .collect();
    visible.sort_by_key(|c| if c.role == "primary" { 0 } else { 1 });

    let thead = visible
        .iter()
        .map(|c| {
            format!(
                "        <th class=\"rio-th--text\">{}</th>",
                esc(&label_of(c))
            )
        })
        .collect::<Vec<_>>()
        .join("\n");

    let tbody = visible
        .iter()
        .enumerate()
        .map(|(i, c)| body_cell(c, i == 0))
        .collect::<Vec<_>>()
        .join("\n");

    let header = format!(
        "{{# ============================================================\n\
         \x20# GENERATED by rustio-design — DO NOT EDIT THIS FILE.\n\
         \x20#\n\
         \x20#   rustio-admin record list (view layer)\n\
         \x20#   project: {project} · table: {table}\n\
         \x20#   mirrors: {based}\n\
         \x20#\n\
         \x20# Source of truth: the [views.{table}] section of rustio.design.toml,\n\
         \x20# decided in design/DESIGN_ARCHITECTURE.md (see DESIGN_REASONING).\n\
         \x20# Edit the spec, then run `rustio-design build`. Hand edits here are\n\
         \x20# reverted on the next build and flagged by `rustio-design check`.\n\
         \x20# Only the column header + body cells are view-spec-driven; all other\n\
         \x20# chrome is reproduced verbatim from the framework template.\n\
         \x20# ============================================================ #}}\n",
        project = project,
        table = view.table,
        based = LIST_TEMPLATE_BASED_ON,
    );

    LIST_BASE
        .replace("__RIO_THEAD_COLS__", &thead)
        .replace("__RIO_TBODY_CELLS__", &tbody)
        .replace("__RIO_HEADER__", header.trim_end())
}

/// Humanise a lead field name for a column header (`booked_at` → `Booked at`).
fn label_of(cell: &ViewCell) -> String {
    let lead = &cell.members[0];
    let mut s = lead.replace('_', " ");
    if let Some(first) = s.get_mut(0..1) {
        first.make_ascii_uppercase();
    }
    s
}

/// Render one `<td>` for a cell, honouring compose style. The first (primary)
/// cell links to the edit page like the framework's leading column.
fn body_cell(cell: &ViewCell, primary: bool) -> String {
    let value = compose_value(cell);
    if primary {
        let lead = &cell.members[0];
        format!(
            "        <td class=\"rio-td--text\">\n\
             \x20         <a class=\"rio-cell-primary\" href=\"/admin/{{{{ admin_name }}}}/{{{{ row.id }}}}/edit\">{value}</a>\n\
             \x20       </td>",
            value = if cell.members.len() == 1 {
                format!("{{{{ row[\"{lead}\"]|default(value=row.id) }}}}")
            } else {
                value
            },
        )
    } else {
        format!("        <td class=\"rio-td--text\">{value}</td>")
    }
}

/// Render a cell's value(s) per compose style using framework-safe markup.
fn compose_value(cell: &ViewCell) -> String {
    let acc = |name: &str| format!("{{{{ row[\"{name}\"]|default(value='') }}}}");
    if cell.members.len() == 1 {
        return acc(&cell.members[0]);
    }
    let lead = acc(&cell.members[0]);
    let trail = &cell.members[1..];
    match cell.style.as_str() {
        "badge" => {
            let pills = trail
                .iter()
                .map(|m| format!("<span class=\"rio-pill\">{}</span>", acc(m)))
                .collect::<Vec<_>>()
                .join(" ");
            format!("<span class=\"rio-cell-compose\">{lead} {pills}</span>")
        }
        "inline" => {
            let rest = trail
                .iter()
                .map(|m| {
                    format!(
                        "<span style=\"color:var(--rio-text-mute)\">{}</span>",
                        acc(m)
                    )
                })
                .collect::<Vec<_>>()
                .join(" · ");
            format!("<span class=\"rio-cell-compose\">{lead} <span style=\"color:var(--rio-text-mute)\">·</span> {rest}</span>")
        }
        // stacked (default): lead on top, trailing muted beneath.
        _ => {
            let rest = trail
                .iter()
                .map(|m| {
                    format!(
                        "<span style=\"display:block;font-size:12px;color:var(--rio-text-mute)\">{}</span>",
                        acc(m)
                    )
                })
                .collect::<Vec<_>>()
                .join("");
            format!("<span class=\"rio-cell-compose\"><span style=\"display:block\">{lead}</span>{rest}</span>")
        }
    }
}

fn esc(s: &str) -> String {
    s.replace('&', "&amp;")
        .replace('<', "&lt;")
        .replace('>', "&gt;")
}

