#[cfg(test)]
mod tests {
    use super::*;
    use crate::spec::Spec;
    use crate::toml_lite::Document;

    fn view(toml: &str) -> TableView {
        Spec::new(Document::parse(toml).unwrap())
            .views()
            .pop()
            .expect("one view")
    }

    #[test]
    fn drives_columns_from_the_spec() {
        let v = view(
            "[views.bookings]\nprimary = \"booked_at\"\n\
             secondary = \"customer + phone (stacked), status (badge)\"\n\
             detail = \"notes\"\nhidden = \"id\"\n",
        );
        let t = build_list_template("Salon", &v);
        // header humanised + present; detail/hidden columns absent
        assert!(t.contains(">Booked at</th>"));
        assert!(t.contains(">Customer</th>"));
        assert!(t.contains(">Status</th>"));
        assert!(!t.contains(">Notes</th>"));
        assert!(!t.contains(">Id</th>"));
        // primary links to edit; compose renders members
        assert!(t.contains("rio-cell-primary"));
        assert!(t.contains("row[\"phone\"]"));
        assert!(t.contains("row[\"status\"]"));
        // no leftover placeholders
        assert!(!t.contains("__RIO_"));
    }

    #[test]
    fn chrome_is_preserved_and_balanced() {
        let v = view("[views.bookings]\nprimary = \"id\"\n");
        let t = build_list_template("X", &v);
        // key chrome features survive verbatim
        for needle in [
            "rio-cmdbar",
            "data-rio-bulk",
            "rio-pagination",
            "{% for group in filters %}",
            "{% block content %}",
        ] {
            assert!(t.contains(needle), "missing chrome: {needle}");
        }
        // minijinja block balance (cheap structural guard). Close needles match
        // both plain and whitespace-trimmed forms (`{% end… %}` / `{%- end… %}`),
        // since the framework macros close with `{%- endmacro %}`.
        assert_eq!(count(&t, "{% for "), count(&t, "endfor %}"));
        assert_eq!(count(&t, "{% block "), count(&t, "endblock %}"));
        assert_eq!(count(&t, "{% macro "), count(&t, "endmacro %}"));
        // block-style `{% if %}` (the inline `{%- if -%}` macro guards close with
        // `{%- endif -%}`, excluded from both sides by the trailing-space needles).
        assert_eq!(count(&t, "{% if "), count(&t, "{% endif %}"));
    }

    fn count(hay: &str, needle: &str) -> usize {
        hay.matches(needle).count()
    }
}
